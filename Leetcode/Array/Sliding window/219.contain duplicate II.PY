
class Solution:
    def containsNearbyDuplicate(self, nums: List[int], k: int) -> bool:
        n = len(nums)
        seen = {}
        for i in range(n):
            if nums[i] in seen and abs(i-seen[nums[i]]) <= k:
                return True
            seen[nums[i]] = i             # store latest index
        return False
